#include "libftprintf.h"

int	ft_putnbr_pos(unsigned int n)
{
	int		i;
	char	c;

	i = 0;
	if (n >= 10)
		i += ft_putnbr(n / 10);
	c = (n % 10) + '0';
	i += write(1, &c, 1);
	return (i);
}
